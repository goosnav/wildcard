// In-app legal + transparency content (REQ-PRIV-001/003). Three documents —
// Privacy Policy, Terms of Service, and an AI-use disclosure — rendered in a
// modal, plus a small links row used on both the signed-out and signed-in
// screens. Content is embedded (no markdown dependency) so it ships in the
// bundle and works offline.
//
// NOTE: these are honest, complete drafts written to the product's actual data
// flows, but they still need a lawyer's sign-off before public launch (OQ-02).
// Update LAST_UPDATED, the contact email, and the governing-law line for your
// entity before going live.

const LAST_UPDATED = "June 12, 2026";
const CONTACT_EMAIL = "support@wildcard.app"; // ← change to your real support address

export type LegalDocId = "privacy" | "terms" | "ai";

export function LegalLinks({ onOpen }: { onOpen: (doc: LegalDocId) => void }) {
  return (
    <span className="legal-links">
      <button onClick={() => onOpen("privacy")}>Privacy</button>
      <span aria-hidden>·</span>
      <button onClick={() => onOpen("terms")}>Terms</button>
      <span aria-hidden>·</span>
      <button onClick={() => onOpen("ai")}>AI&nbsp;use</button>
    </span>
  );
}

const TITLES: Record<LegalDocId, string> = {
  privacy: "Privacy Policy",
  terms: "Terms of Service",
  ai: "How Wild Card uses AI",
};

export function LegalModal({ doc, onClose }: { doc: LegalDocId; onClose: () => void }) {
  return (
    <div className="legal-overlay" onClick={onClose}>
      <div className="legal-modal" onClick={(e) => e.stopPropagation()}>
        <div className="legal-head">
          <h2>{TITLES[doc]}</h2>
          <button className="legal-close" onClick={onClose} aria-label="Close">
            ✕
          </button>
        </div>
        <div className="legal-body">
          <p className="legal-meta">Last updated: {LAST_UPDATED}</p>
          {doc === "privacy" && <Privacy />}
          {doc === "terms" && <Terms />}
          {doc === "ai" && <AiUse />}
        </div>
      </div>
    </div>
  );
}

