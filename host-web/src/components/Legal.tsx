function AiUse() {
  return (
    <>
      <p>
        Wild Card builds tools using a large language model. We want to be clear about
        what that means for you.
      </p>
      <h3>What gets sent to the AI</h3>
      <p>
        When you describe a tool (or ask to edit one), your description and — for edits —
        the current tool’s source are sent to our AI provider to generate the result. Your
        email and the data your tools save locally are <strong>not</strong> sent to the
        model.
      </p>
      <h3>AI output can be wrong</h3>
      <p>
        Generated tools are produced automatically and are not reviewed by a person. They
        can contain mistakes. The source is always viewable and editable so you can check
        and change what a tool does before relying on it.
      </p>
      <h3>Safety</h3>
      <p>
        We screen requests and refuse ones that appear intended to cause harm or break the
        law. Every tool is validated in a locked-down sandbox before it’s delivered, and
        tools cannot reach the network except through a small set of vetted data sources.
      </p>
      <h3>No background data access</h3>
      <p>
        Tools run in an isolated sandbox. They cannot read your other tools’ data, your
        files, or your device.
      </p>
    </>
  );
}
